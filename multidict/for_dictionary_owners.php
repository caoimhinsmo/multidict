<h1 class="smo">A note for dictionary owners</h1>

<p>We hope you will like Multidict.  It does not “skim” or alter your pages in any way, but links to them and will drive traffic to your dictionary.</p>

<p>If Multidict does not currently link to your dictionary, and you would like us to try to include it, send us an e-mail (caoimhin@smo.uhi.ac.uk) and we will have a look.  Some dictionaries are much easier to link to than others.  Some dictionaries too, work much better with Wordlink than others - generally those which are uncluttered and will display their results easily within a narrow window.</p>

<p>If your dictionary is currently linked to by Multidict and you would like us to remove it for any reason, just let us know and we will do that.
</p>

