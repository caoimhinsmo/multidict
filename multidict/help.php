<h1 style="font-size:120%">Using Multidict</h1>

<p>Multidict makes it easy to swap between various freely available online dictionaries in a variety of languages.</p>

<div class="box">Multidict makes use of JavaScript to provide interactivity, and cookies to remember the your previous choices of dictionaries and target languages.  If you have JavaScript or Cookies disabled in your browser, Multidict will let you know.  It will still work, but will not work nearly as conveniently and not all of the facilities described below will be available.</div>

<p>You can click any of the dictionary “favicons” below the Dictionary select box to switch to another dictionary.</p>

<p>You can click the ‘↔’ symbol to swop the “From” and “To” languages.</p>

<p>Icons for closely related languages which may have useful dictionaries are shown below the “From” and “To” selection boxes.  You can click the icon to quickly switch to this language.</p>

<p>See the <a href="languages.php">languages</a> list to see all the languages currently handled by Multidict, ordered by family and language group.</p>

